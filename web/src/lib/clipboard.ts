// Copy text to the clipboard. navigator.clipboard only exists in a secure
// context (HTTPS or localhost); a self-hosted panel is often served over plain
// HTTP, where it's undefined. Fall back to a hidden-textarea + execCommand so
// copy works there too. Returns whether it succeeded.
export async function copyText(text: string): Promise<boolean> {
  try {
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(text);
      return true;
    }
  } catch {
    // fall through to the legacy path
  }

  try {
    const ta = document.createElement("textarea");
    ta.value = text;
    ta.setAttribute("readonly", "");
    ta.style.position = "fixed";
    ta.style.top = "-9999px";
    document.body.appendChild(ta);
    ta.focus(); // some browsers require focus before execCommand('copy') will fire
    ta.select();
    const ok = document.execCommand("copy");
    document.body.removeChild(ta);
    return ok;
  } catch {
    return false;
  }
}
